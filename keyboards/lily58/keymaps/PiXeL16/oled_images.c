// OLED image management state
static oled_image_state_t oled_image_state = {0};

void oled_images_init(void) {
    oled_image_state.current_image = 0;
    oled_image_state.image_timer = timer_read32();
}

void oled_images_update(void) {
    // Change image every rotation time
    if (timer_elapsed32(oled_image_state.image_timer) > OLED_IMAGE_ROTATION_TIME) {
        oled_image_state.current_image = (oled_image_state.current_image + 1) % (NUM_OLED_IMAGES + 1); // +1 for bongo cat
        oled_image_state.image_timer = timer_read32();
    }
}

uint8_t oled_images_get_current(void) {
    return oled_image_state.current_image;
}

void oled_images_draw_current(void) {
    switch (oled_image_state.current_image) {
        case 0:
            oled_write_raw_P(ichigo_logo, sizeof(ichigo_logo));
            break;
        case 1:
            oled_write_raw_P(pixel16_logo, sizeof(pixel16_logo));
            break;
        case 2:
            oled_write_raw_P(third_logo, sizeof(third_logo));
            break;
        // Case 3 (bongo cat) is handled in the main keymap
    }
}

bool oled_images_should_clear_for_bongocat(void) {
    static uint8_t last_image = 255; // Initialize with invalid value
    bool should_clear = false;
    
    if (oled_image_state.current_image == 3 && last_image != 3) {
        // Switching to bongo cat
        should_clear = true;
    } else if (oled_image_state.current_image != 3 && last_image == 3) {
        // Switching away from bongo cat
        should_clear = true;
    }
    
    last_image = oled_image_state.current_image;
    return should_clear;
}

